import axios from "axios";
import { useEffect, useRef, useCallback } from "react";
import { useNavigate } from "react-router-dom";

import { useApp } from "../../hooks/useApp";
import { api } from "../../services/api";

import type { PatientRecord } from "../../types/patient";

import { PatientTable } from "../../components/PatientTable/PatientTable";
import { VitalChart } from "../../components/VitalChart/VitalChart";
import { MapView } from "../../components/MapView/MapView";
import FileList from "../../components/FileList/FileList";
import { 
    ChartSection,
    DashboardContainer,
    DashboardGrid,
    DashboardHeader,
    ErrorMessage,
    FileInfo,
    LoadingMessage,
    MapSection,
    TableSection
} from "./DashboardPage.styles";

export function DashboardPage() {
    const navigate = useNavigate();
    const { state, setPatientData, setUploadedFiles, setLoading, setError } = useApp();
    
    // üî• CONTROLE CENTRALIZADO DE ESTADO
    const hasLoadedPatients = useRef(false);
    const hasLoadedFiles = useRef(false);
    const isFetching = useRef(false);

    // üî• FUN√á√ÉO √öNICA PARA CARREGAR ARQUIVOS
    const loadFiles = useCallback(async () => {
        if (hasLoadedFiles.current) {
            console.log("‚è© Lista de arquivos j√° carregada, ignorando...");
            return;
        }

        try {
            console.log("üìÅ Buscando lista de arquivos...");
            const response = await api.get("/upload/files");
            const files = response.data.files || [];
            console.log("‚úÖ Arquivos encontrados:", files);
            
            setUploadedFiles(files);
            hasLoadedFiles.current = true;
        } catch (error) {
            console.error("‚ùå Erro ao carregar arquivos:", error);
            // N√£o seta error global para n√£o quebrar o dashboard
        }
    }, [setUploadedFiles]);

    // üî• FUN√á√ÉO √öNICA PARA CARREGAR PACIENTES
    const loadPatients = useCallback(async () => {
        if (isFetching.current || hasLoadedPatients.current) {
            console.log("‚è© Dados de pacientes j√° carregados, ignorando...");
            return;
        }

        isFetching.current = true;
        setLoading(true);
        setError(null);

        try {
            console.log("üìä Buscando dados de pacientes...");
            const response = await api.get("/patients/records");
            const allData: PatientRecord[] = response.data;
            console.log("‚úÖ Dados de pacientes recebidos:", allData.length, "registros");

            // üî• FILTRAGEM CENTRALIZADA
            let filteredData = allData;
            if (state.selectedFile) {
                filteredData = allData.filter(item => item.source_file === state.selectedFile);
                console.log("üîç Filtrado por arquivo:", state.selectedFile, "-", filteredData.length, "registros");
            }

            setPatientData(filteredData);
            hasLoadedPatients.current = true;

        } catch (error) {
            let msg = "Erro ao carregar os dados";
            if (axios.isAxiosError(error)) {
                console.error("AxiosError:", error.message);
                msg = `Erro de conex√£o: ${error.message}`;
            } else {
                console.error("Erro desconhecido:", error);
                msg = error instanceof Error ? error.message : msg;
            }
            setError(msg);
        } finally {
            setLoading(false);
            isFetching.current = false;
        }
    }, [state.selectedFile, setPatientData, setLoading, setError]);

    // üî• CARREGAMENTO INICIAL - UMA √öNICA VEZ
    useEffect(() => {
        const initializeDashboard = async () => {
            if (!state.isValidFile) {
                console.log("‚ö†Ô∏è Redirecionando para home - arquivo n√£o v√°lido");
                navigate("/");
                return;
            }

            console.log("üöÄ INICIANDO DASHBOARD - Carregamento inicial");
            await loadFiles();
            await loadPatients();
        };

        initializeDashboard();
    }, [state.isValidFile, navigate]); // üî• DEPEND√äNCIAS M√çNIMAS

    // üî• ATUALIZA√á√ÉO QUANDO ARQUIVO SELECIONADO MUDAR
    useEffect(() => {
        if (hasLoadedPatients.current && state.selectedFile) {
            console.log("üîÑ Arquivo selecionado mudou, recarregando dados...");
            hasLoadedPatients.current = false; // Permite recarregar
            loadPatients();
        }
    }, [state.selectedFile, loadPatients]);

    // üî• REFRESH MANUAL - CONTROLADO
    const handleRefresh = useCallback(async () => {
        console.log("üîÑ SOLICITA√á√ÉO DE REFRESH MANUAL");
        
        // Reset dos controles para permitir recarregamento
        hasLoadedPatients.current = false;
        hasLoadedFiles.current = false;
        
        await loadFiles();
        await loadPatients();
    }, [loadFiles, loadPatients]);

    // üî• DEBUG: Monitoramento do estado
    useEffect(() => {
        console.log("üìä ESTADO DO DASHBOARD:", {
            isValidFile: state.isValidFile,
            loading: state.loading,
            patientData: state.patientData.length,
            uploadedFiles: state.uploadedFiles.length,
            selectedFile: state.selectedFile,
            hasLoadedPatients: hasLoadedPatients.current,
            hasLoadedFiles: hasLoadedFiles.current,
            isFetching: isFetching.current
        });
    }, [state.isValidFile, state.loading, state.patientData.length, state.uploadedFiles.length, state.selectedFile]);

    return (
        <DashboardContainer>
            <DashboardHeader>
                <div>
                    <h2>üìä Dashboard de Monitoramento</h2>
                    {state.selectedFile ? (
                        <FileInfo>
                            Visualizando: <strong>{state.selectedFile}</strong>
                            <button 
                                onClick={handleRefresh}
                                disabled={state.loading}
                                style={{
                                    marginLeft: "1rem",
                                    padding: "0.3rem 0.8rem",
                                    background: state.loading ? "#ccc" : "#667eea",
                                    color: "white",
                                    border: "none",
                                    borderRadius: "5px",
                                    cursor: state.loading ? "not-allowed" : "pointer"
                                }}
                            >
                                {state.loading ? "‚è≥..." : "üîÑ Atualizar"}
                            </button>
                        </FileInfo>
                    ) : (
                        <FileInfo>
                            Visualizando: <strong>Todos os arquivos</strong>
                            <span style={{ marginLeft: "1rem", color: "#718096" }}>
                                ({state.patientData.length} registros)
                            </span>
                        </FileInfo>
                    )}
                </div>
                {/* üî• FileList agora √© apenas UI - n√£o carrega dados */}
                <FileList />
            </DashboardHeader>

            {state.loading && <LoadingMessage>‚è≥ Carregando dados...</LoadingMessage>}
            
            {state.error && (
                <ErrorMessage>
                    ‚ùå {state.error}
                    <br />
                    <small>Verifique se o backend est√° rodando</small>
                </ErrorMessage>
            )}

            {/* RENDERIZA√á√ÉO DOS COMPONENTES */}
            {!state.loading && !state.error && state.patientData.length > 0 && (
                <>
                    <ChartSection>
                        <VitalChart />
                    </ChartSection>
                    
                    <DashboardGrid>
                        <TableSection>
                            <PatientTable />
                        </TableSection>
                        
                        <MapSection>
                            <MapView />
                        </MapSection>
                    </DashboardGrid>
                </>
            )}

            {!state.loading && !state.error && state.patientData.length === 0 && (
                <div style={{ textAlign: "center", padding: "2rem" }}>
                    <p>üìù Nenhum dado dispon√≠vel para exibi√ß√£o</p>
                    <button 
                        onClick={handleRefresh}
                        style={{
                            marginTop: "1rem",
                            padding: "0.5rem 1rem",
                            background: "#667eea",
                            color: "white",
                            border: "none",
                            borderRadius: "5px",
                            cursor: "pointer"
                        }}
                    >
                        üîÑ Tentar Novamente
                    </button>
                </div>
            )}
        </DashboardContainer>
    );
}























